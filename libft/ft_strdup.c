#include "libft.h"

char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(const char *s1)
{
	char	*dest;

	dest = malloc((ft_strlen((char *)s1) + 1) * sizeof(char));
	if (!dest)
		return (NULL);
	ft_strcpy(dest, (char *)s1);
	return (dest);
}
